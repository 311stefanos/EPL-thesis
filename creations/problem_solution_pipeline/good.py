""" Schemas """

class TestResultEntry(TypedDict):
	"""
	Represents a single test result entry for a sample. Captures whether the sample passed all test cases, optional error message, number of tests passed, and total number of tests. Used as the value type in the test_results dictionary within AgentSchema.
	"""
	passed: bool # True if the sample passed all test cases, otherwise False.
	error: Optional[str] # Optional error message when passed is False; provides details about the failure.
	number_tests_passed: int # Number of test cases that passed for this sample.
	total_tests: int # Total number of test cases for this sample.

class AgentSchema(TypedDict):
	"""
	Main state schema for the problem_solution_pipeline graph. It aggregates all keys used across nodes: problem specification (function_signature, docstring, test_cases, optional task_id), generation artifacts (samples, generation_attempt), validation artifacts (valid_samples, invalid_samples, syntax_validation_passed), test execution results (test_results, total_passed, total_failed), repair tracking (repair_round, repair_feedback, should_repair), and final output (final_solution). This TypedDict serves as the unified state object for the LangGraph workflow.
	"""
	function_signature: str # <- Target function signature from the problem spec; used by generate_samples and finalize_solution.
	docstring: str # <- NaturalГѓВўГўвЂљВ¬Гўв‚¬Лњlanguage description of required behavior; used by generate_samples and finalize_solution.
	test_cases: List[Any] # <- List of test cases defining expected inputs/outputs; consumed by generate_samples, execute_tests, and finalize_solution.
	task_id: Optional[str] # <- Optional identifier for the problem (metadata); passed through to finalize_solution for solution_metadata.
	samples: List[str] # Raw code strings generated by the LLM; produced by generate_samples and consumed by validate_syntax, execute_tests, repair_samples, and finalize_solution.
	generation_attempt: int # Counter tracking how many times generation has been performed; used in generate_samples to monitor progress.
	valid_samples: List[str] # Samples that passed syntax validation; output of validate_syntax and input to execute_tests.
	invalid_samples: List[str] # Samples that raised SyntaxError; output of validate_syntax (kept for debugging).
	syntax_validation_passed: bool # Flag indicating whether any syntactically valid samples remain; used to decide if testing should proceed.
	test_results: Dict[int, TestResultEntry] # Mapping from sample index to its test outcome (passed flag, optional error, number_tests_passed, total_tests); produced by execute_tests and consumed by analyze_failures and repair_samples.
	total_passed: int # Number of samples that passed all tests; computed in execute_tests and used in analyze_failures.
	total_failed: int # Number of samples that failed at least one test; computed in execute_tests and used in analyze_failures.
	repair_round: int # Current repair round count (default 0); incremented in repair_samples and tracked in analyze_failures to enforce the 2ГѓВўГўвЂљВ¬Гўв‚¬Лњround limit.
	repair_feedback: str # Guidance generated by analyze_failures_llm on how to fix failing samples; consumed by repair_samples.
	should_repair: bool # Flag indicating whether a repair step should be triggered; set by analyze_failures and used for conditional routing.
	final_solution: str # Complete Python function code assembled by finalize_solution; the ultimate output of the pipeline.


