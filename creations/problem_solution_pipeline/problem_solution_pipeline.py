def extract_python_code_blocks(content: str) -> List[str]:
    """
    Extract separate Python samples from an LLM response.

    Supports fenced blocks labelled as python, py, or without a language tag.
    If no complete fenced blocks exist, treats the cleaned response as one
    candidate.
    """
    if not isinstance(content, str):
        content = str(content)

    pattern = re.compile(
        r"```(?:python|py)?[ \t]*\r?\n(.*?)```",
        flags=re.IGNORECASE | re.DOTALL,
    )

    samples = [
        match.group(1).strip()
        for match in pattern.finditer(content)
        if match.group(1).strip()
    ]

    if samples:
        return samples

    cleaned = clean_llm_output(content).strip()
    return [cleaned] if cleaned else []

def validate_code_syntax(code: str) -> bool:
	"""
	Overview: Checks if a given Python code string is syntactically valid using ast.parse.
	Caller Node: validate_syntax
	
	Instructions: Retrieve the code string, attempt to parse it with ast.parse, catch SyntaxError, return True if parsing succeeds, False otherwise.
	
	Args: code (str): The Python source code to validate.
	
	Returns: bool: True if syntax is valid, False otherwise.
	"""
	try:
		ast.parse(code)
		return True
	except (SyntaxError, ValueError):
		return False


def run_code_in_isolated_sandbox(
    code: str,
    test_cases: list,
    function_signature: str,
    *,
    timeout_seconds: int = 8,
    memory_limit: str = "256m",
    cpus: str = "1.0",
    pids_limit: int = 64,
    docker_image: str = "python:3.11-slim",
) -> dict:
    """
    Executes generated Python code inside a locked-down Docker container.

    Supports test cases:
    1. "assert add(1, 2) == 3"
    2. {"inputs": [1, 2], "expected": 3}
    3. {"input": [1, 2, 3], "expected": 6}
    4. [[1, 2], 3]  # legacy tuple/list style

    Returns:
    {
        "passed": bool,
        "error": str | None,
        "number_tests_passed": int,
        "total_tests": int,
        "test_results": list
    }
    """
    total_tests = len(test_cases)

    if shutil.which("docker") is None:
        return {
            "passed": False,
            "error": "Docker is not installed or not available on PATH.",
            "number_tests_passed": 0,
            "total_tests": total_tests,
            "test_results": [],
        }

    try:
        payload_json = json.dumps(
            {
                "code": code,
                "test_cases": test_cases,
                "function_signature": function_signature,
            },
            ensure_ascii=False,
        )
    except TypeError as exc:
        return {
            "passed": False,
            "error": f"test_cases must be JSON-serializable: {exc}",
            "number_tests_passed": 0,
            "total_tests": total_tests,
            "test_results": [],
        }

    runner_code = r'''
import ast
import json
import os
import sys


original_stdout_fd = os.dup(1)


def safe_emit(payload):
    try:
        data = json.dumps(payload, ensure_ascii=False)
    except Exception as exc:
        data = json.dumps({
            "passed": False,
            "error": f"Could not serialize sandbox result: {exc}",
            "number_tests_passed": 0,
            "total_tests": 0,
            "test_results": [],
        })

    os.write(original_stdout_fd, data.encode("utf-8") + b"\n")


try:
    devnull_fd = os.open(os.devnull, os.O_WRONLY)
    os.dup2(devnull_fd, 1)
    os.dup2(devnull_fd, 2)
    sys.stdout = open(os.devnull, "w", encoding="utf-8")
    sys.stderr = open(os.devnull, "w", encoding="utf-8")

    with open("/sandbox/payload.json", "r", encoding="utf-8") as f:
        payload = json.load(f)

    code = payload["code"]
    test_cases = payload["test_cases"]
    function_signature = payload["function_signature"]
    total_tests = len(test_cases)

    namespace = {
        "__name__": "__submitted_solution__",
    }

    try:
        compiled_code = compile(code, "/sandbox/submitted_solution.py", "exec")
        exec(compiled_code, namespace)
    except BaseException as exc:
        safe_emit({
            "passed": False,
            "error": f"Code execution failed: {type(exc).__name__}: {exc}",
            "number_tests_passed": 0,
            "total_tests": total_tests,
            "test_results": [],
        })
        raise SystemExit(0)

    try:
        signature = function_signature.strip()

        if signature.isidentifier():
            function_name = signature
        else:
            try:
                tree = ast.parse(signature)
            except SyntaxError:
                if signature.startswith("def ") and signature.endswith(":"):
                    tree = ast.parse(signature + "\n    pass")
                else:
                    raise

            first_node = tree.body[0]

            if isinstance(first_node, ast.FunctionDef):
                function_name = first_node.name
            elif isinstance(first_node, ast.Expr):
                expression = first_node.value

                if isinstance(expression, ast.Name):
                    function_name = expression.id
                elif isinstance(expression, ast.Call) and isinstance(expression.func, ast.Name):
                    function_name = expression.func.id
                else:
                    raise ValueError("Could not extract function name.")
            else:
                raise ValueError("Could not extract function name.")

        target_function = namespace.get(function_name)

        if target_function is None or not callable(target_function):
            raise ValueError(f"Function '{function_name}' not found or not callable.")

    except BaseException as exc:
        safe_emit({
            "passed": False,
            "error": f"Failed to extract function: {type(exc).__name__}: {exc}",
            "number_tests_passed": 0,
            "total_tests": total_tests,
            "test_results": [],
        })
        raise SystemExit(0)

    passed_tests = 0
    error_messages = []
    test_results = []

    for index, test_case in enumerate(test_cases, start=1):
        try:
            if isinstance(test_case, str):
                stripped = test_case.strip()

                if not stripped.startswith("assert") and "check(" not in stripped:
                    raise ValueError(
                        "String test cases must be assert statements "
                        "or HumanEval-style check(candidate) tests."
                    )

                test_namespace = dict(namespace)
                test_namespace["candidate"] = target_function
                test_namespace[function_name] = target_function

                compiled_test = compile(
                    test_case,
                    f"/sandbox/test_{index}.py",
                    "exec",
                )

                exec(compiled_test, test_namespace)

                passed_tests += 1
                test_results.append({
                    "test": index,
                    "passed": True,
                    "error": None,
                })

            else:
                if isinstance(test_case, dict):
                    if "expected" not in test_case:
                        raise ValueError("Dict test case must contain 'expected'.")

                    expected = test_case["expected"]

                    if "inputs" in test_case:
                        inputs = test_case["inputs"]

                        if not isinstance(inputs, list):
                            raise ValueError("'inputs' must be a list.")

                        result = target_function(*inputs)

                    elif "input" in test_case:
                        result = target_function(test_case["input"])

                    else:
                        raise ValueError("Dict test case must contain 'input' or 'inputs'.")

                elif isinstance(test_case, list) and len(test_case) == 2:
                    inputs, expected = test_case

                    if isinstance(inputs, list):
                        result = target_function(*inputs)
                    else:
                        result = target_function(inputs)

                else:
                    raise ValueError(f"Invalid test case format: {test_case!r}")

                if result == expected:
                    passed_tests += 1
                    test_results.append({
                        "test": index,
                        "passed": True,
                        "error": None,
                    })
                else:
                    message = f"Expected {expected!r}, got {result!r}"
                    error_messages.append(f"Test {index}: {message}")
                    test_results.append({
                        "test": index,
                        "passed": False,
                        "error": message,
                    })

        except AssertionError as exc:
            message = str(exc) or "Assertion failed"
            error_messages.append(f"Test {index}: {message}")
            test_results.append({
                "test": index,
                "passed": False,
                "error": message,
            })

        except BaseException as exc:
            message = f"{type(exc).__name__}: {exc}"
            error_messages.append(f"Test {index}: {message}")
            test_results.append({
                "test": index,
                "passed": False,
                "error": message,
            })

    safe_emit({
        "passed": len(error_messages) == 0,
        "error": "\n".join(error_messages) if error_messages else None,
        "number_tests_passed": passed_tests,
        "total_tests": total_tests,
        "test_results": test_results,
    })

except BaseException as exc:
    safe_emit({
        "passed": False,
        "error": f"Sandbox runner crashed: {type(exc).__name__}: {exc}",
        "number_tests_passed": 0,
        "total_tests": 0,
        "test_results": [],
    })
'''

    container_name = f"code-sandbox-{uuid.uuid4().hex}"

    with tempfile.TemporaryDirectory(prefix="code_sandbox_") as temp_dir:
        temp_path = Path(temp_dir)

        runner_path = temp_path / "runner.py"
        payload_path = temp_path / "payload.json"

        runner_path.write_text(runner_code, encoding="utf-8")
        payload_path.write_text(payload_json, encoding="utf-8")

        try:
            os.chmod(temp_path, 0o755)
            os.chmod(runner_path, 0o644)
            os.chmod(payload_path, 0o644)
        except OSError:
            pass

        docker_command = [
            "docker",
            "run",
            "--rm",
            "--name",
            container_name,

            # No network.
            "--network",
            "none",

            # Resource limits.
            "--memory",
            memory_limit,
            "--memory-swap",
            memory_limit,
            "--cpus",
            cpus,
            "--pids-limit",
            str(pids_limit),

            # Lower privileges.
            "--cap-drop",
            "ALL",
            "--security-opt",
            "no-new-privileges:true",
            "--user",
            "65534:65534",

            # Read-only container filesystem.
            "--read-only",

            # Small writable temp area.
            "--tmpfs",
            "/tmp:rw,noexec,nosuid,size=64m",

            # Mount only runner and payload.
            "--mount",
            f"type=bind,source={str(temp_path.resolve())},target=/sandbox,readonly",

            "--workdir",
            "/sandbox",

            "-e",
            "PYTHONDONTWRITEBYTECODE=1",
            "-e",
            "HOME=/tmp",

            docker_image,
            "python",
            "/sandbox/runner.py",
        ]

        try:
            process = subprocess.Popen(
                docker_command,
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                text=True,
            )

            stdout, stderr = process.communicate(timeout=timeout_seconds)

        except subprocess.TimeoutExpired:
            try:
                subprocess.run(
                    ["docker", "rm", "-f", container_name],
                    stdout=subprocess.DEVNULL,
                    stderr=subprocess.DEVNULL,
                    timeout=3,
                )
            except Exception:
                pass

            return {
                "passed": False,
                "error": f"Sandbox timeout after {timeout_seconds} seconds.",
                "number_tests_passed": 0,
                "total_tests": total_tests,
                "test_results": [],
            }

        except Exception as exc:
            return {
                "passed": False,
                "error": f"Failed to start Docker sandbox: {type(exc).__name__}: {exc}",
                "number_tests_passed": 0,
                "total_tests": total_tests,
                "test_results": [],
            }

        stdout = (stdout or "").strip()
        stderr = (stderr or "").strip()

        if not stdout:
            return {
                "passed": False,
                "error": (
                    f"Sandbox produced no result. Exit code: {process.returncode}."
                    + (f"\nStderr: {stderr[:2000]}" if stderr else "")
                ),
                "number_tests_passed": 0,
                "total_tests": total_tests,
                "test_results": [],
            }

        try:
            return json.loads(stdout.splitlines()[-1])
        except Exception:
            return {
                "passed": False,
                "error": (
                    "Sandbox did not return valid JSON."
                    f"\nExit code: {process.returncode}"
                    f"\nStdout: {stdout[:2000]}"
                    f"\nStderr: {stderr[:2000]}"
                ),
                "number_tests_passed": 0,
                "total_tests": total_tests,
                "test_results": [],
            }
