def ocr_processing(state: AgentSchema) -> AgentSchema:
    """ Execution: LLM+TOOLS. Processes receipt photos using OCR (OpenRouter or PaddleOCR) to extract cost, date, items, location, and category. If OCR fails or receipt is unclear, sets state.mode to 'ocr_failed' and transitions to chat to notify user. On success, writes extracted data to the local Excel file using excel_io tool and transitions to chat for confirmation reply. 
    Processes receipt photos using OCR to extract structured data (cost, date, items, location, category) and transitions to chat for confirmation or error handling. This node is triggered when a receipt photo is detected in the incoming message.
    
    Steps:
    1. Check if the last message contains a receipt photo (via state.mode or message content).
    2. If yes, invoke OCR using OpenRouter or PaddleOCR to extract fields.
    3. Validate extracted data; if invalid or low confidence, set state.mode to 'ocr_failed'.
    4. If valid, format items as 'item1 (quantity x price currency), ...' and set state.next_action to 'confirm_receipt'.
    5. Transition to chat node for user reply.
    
    Inputs:
    - state.messages: List of conversation messages; last message may contain image.
    - state.mode: Indicates if processing a receipt (e.g., 'process_receipt').
    - state.last_seen_event_id: To avoid reprocessing duplicates.
    
    Outputs:
    - Updated state with extracted receipt data in messages or error state.
    - state.next_action: Set to 'confirm_receipt' or 'ocr_failed'.
    - Appends OCR result or error message to messages.
    
    Helpful Functions:
    - extract_receipt_data: Preprocess image and run OCR.
    - validate_ocr_result: Check confidence and completeness.
    - format_items: Convert raw items to required string format.
    
    Note: Uses ToolNode for tool execution; LLM may call tools during processing.
    """

    print_function_name()
    try:
        # Get the last message to check for image content
        last_message = state['messages'][-1]
        
        # Extract image content from the last message
        image_content = None
        if hasattr(last_message, 'content') and isinstance(last_message.content, list):
            # Handle multi-modal content (list of content blocks)
            for content_block in last_message.content:
                if isinstance(content_block, dict) and content_block.get('type') == 'image_url':
                    image_url = content_block['image_url']['url']
                    # Download image from URL if it's a URL, otherwise use as file path
                    if image_url.startswith('http://') or image_url.startswith('https://'):
                        
                        response = requests.get(image_url, timeout=10)
                        image_content = response.content
                    else:
                        image_content = image_url
                    break
        elif hasattr(last_message, 'content') and isinstance(last_message.content, (str, bytes)):
            # Handle raw string (file path) or bytes content
            image_content = last_message.content
        
        # If no image content found, try to extract from message additional_kwargs
        if image_content is None and hasattr(last_message, 'additional_kwargs'):
            image_content = last_message.additional_kwargs.get('image', None)
        
        # Check if we have image content to process
        if image_content is None:
            return {
                'mode': 'ocr_failed',
                'next_action': 'ocr_failed',
                'messages': [AIMessage(content="No receipt image found in the message. Please send a clear photo of your receipt.")],
                'latest': "No receipt image found in the message. Please send a clear photo of your receipt."
            }
        
        # Use extract_receipt_data helper function to extract raw OCR data
        raw_ocr_data = extract_receipt_data(image_content)
        
        # Validate the extracted OCR data
        is_valid, validation_message = validate_ocr_result(raw_ocr_data)
        
        if not is_valid:
            # OCR validation failed
            return {
                'mode': 'ocr_failed',
                'next_action': 'ocr_failed',
                'messages': [AIMessage(content=f"OCR processing failed: {validation_message}. Please provide a clearer receipt image or enter the details manually.")],
                'latest': f"OCR processing failed: {validation_message}. Please provide a clearer receipt image or enter the details manually."
            }
        
        # Format the OCR processing prompt with extracted data for LLM refinement
        prompt = prompts.OCR_PROCESSING_PROMPT.format(
            extracted_data=json.dumps(raw_ocr_data, indent=2),
            mode=state.get('mode', 'unknown')
        )
        
        # Prepare messages for the LLM
        messages = [
            SystemMessage(content=prompt),
            last_message
        ]
        
        # Invoke the OCR processing LLM with structured output to refine the extracted data
        result = safe_invoke(ocr_processing_llm, messages=messages)
        
        # Extract the structured ReceiptData from the result
        if isinstance(result, AIMessage):
            receipt_data = result.content
        else:
            receipt_data = result
        
        # If LLM returns None or invalid result, use the raw OCR data
        if receipt_data is None:
            # Try to create ReceiptData from raw_ocr_data
            try:
                # Format items using the helper function if items is a list
                items = raw_ocr_data.get('items')
                if isinstance(items, list):
                    items = format_items(items)
                
                receipt_data = ReceiptData(
                    cost=raw_ocr_data.get('cost', 0.0),
                    date=raw_ocr_data.get('date', ''),
                    items=items or '',
                    location=raw_ocr_data.get('location', ''),
                    category=raw_ocr_data.get('category', '')
                )
            except Exception as e:
                return {
                    'mode': 'ocr_failed',
                    'next_action': 'ocr_failed',
                    'messages': [AIMessage(content=f"Failed to process receipt data: {str(e)}. Please try again with a clearer image.")],
                    'latest': f"Failed to process receipt data: {str(e)}. Please try again with a clearer image."
                }
        
        # Convert ReceiptData to dict for final validation and formatting
        if isinstance(receipt_data, ReceiptData):
            ocr_data_dict = {
                'cost': receipt_data.cost,
                'date': receipt_data.date,
                'items': receipt_data.items,
                'location': receipt_data.location,
                'category': receipt_data.category
            }
        else:
            ocr_data_dict = receipt_data
            # Format items if it's a list
            if isinstance(ocr_data_dict.get('items'), list):
                ocr_data_dict['items'] = format_items(ocr_data_dict['items'])
        
        # Final validation after LLM processing
        is_valid_final, validation_message_final = validate_ocr_result(ocr_data_dict)
        
        if not is_valid_final:
            return {
                'mode': 'ocr_failed',
                'next_action': 'ocr_failed',
                'messages': [AIMessage(content=f"OCR processing failed after refinement: {validation_message_final}. Please provide a clearer receipt image.")],
                'latest': f"OCR processing failed after refinement: {validation_message_final}. Please provide a clearer receipt image."
            }
        
        # OCR validation succeeded - prepare success response
        formatted_message = (
            f"Receipt processed successfully!\n\n"
            f"Location: {ocr_data_dict['location']}\n"
            f"Date: {ocr_data_dict['date']}\n"
            f"Total Cost: {ocr_data_dict['cost']}\n"
            f"Items: {ocr_data_dict['items']}\n"
            f"Category: {ocr_data_dict['category']}\n\n"
            f"Is this information correct?"
        )
        
        latest_message = (
            f"Receipt processed successfully! Location: {ocr_data_dict['location']}, "
            f"Date: {ocr_data_dict['date']}, Total Cost: {ocr_data_dict['cost']}, "
            f"Category: {ocr_data_dict['category']}. Is this information correct?"
        )
        
        return {
            'next_action': 'confirm_receipt',
            'receipt_data': receipt_data if isinstance(receipt_data, ReceiptData) else ReceiptData(**ocr_data_dict),
            'messages': [AIMessage(content=formatted_message)],
            'latest': latest_message
        }
        
    except Exception as e:
        print(f'{RED}[NODE] [ERR]{RESET}', e) if DEBUG else None
        traceback.print_exc() if DEBUG else None
        
        # Handle exceptions by returning error state
        return {
            'mode': 'ocr_failed',
            'next_action': 'ocr_failed',
            'messages': [AIMessage(content=f"An error occurred while processing the receipt: {str(e)}. Please try again with a clearer image.")],
            'latest': f"An error occurred while processing the receipt: {str(e)}. Please try again with a clearer image."
        }


def chat(state: AgentSchema) -> AgentSchema:
    """ Execution: LLM+TOOLS. Main conversational agent that generates user-facing replies. Uses tools: tavily_search (for context if needed), excel_io (read/write local Excel file), currency_converter (static rates for EUR conversion). Handles two main cases: 1) After OCR processing, confirms receipt data was saved or notifies user of OCR failure. 2) Responds to spending queries by retrieving data from local Excel file and calculating answers. All replies are sent via external Green-API integration. 
    Main conversational agent that generates user-facing replies. Handles two cases: confirming receipt data after OCR, or answering spending queries from Excel. Uses tools for search, Excel I/O, and currency conversion. All replies are sent via external Green-API.
    
    Steps:
    1. Read state.messages and state.next_action to determine context.
    2. If next_action is 'confirm_receipt', generate confirmation message with extracted data.
    3. If next_action is 'ocr_failed', notify user and ask for clarification.
    4. If user query (e.g., 'how much spent on groceries?'), use excel_io to read data and calculate answer.
    5. For ambiguous queries, use tavily_search for context if needed.
    6. Format reply in English, using Euro as primary currency.
    7. Append reply to messages as AIMessage.
    8. Set state.next_action if follow-up needed, else clear it.
    9. Transition to END.
    
    Inputs:
    - state.messages: Conversation history, including last user message.
    - state.next_action: Indicates context (e.g., 'confirm_receipt', 'ocr_failed', or query type).
    - state.pending_question: If clarification is needed.
    
    Outputs:
    - state.messages: Appended with assistant reply.
    - state.next_action: Updated or cleared based on response.
    - state.latest: The generated reply text.
    
    Tools:
    - tavily_search: For external context if query is ambiguous.
    - excel_io: To read/write local Excel file for spending data.
    - currency_converter: Static rates for EUR conversion.
    
    Helpful Functions:
    - parse_spending_query: Extract category, time period from user question.
    - calculate_spending: Sum costs from Excel based on filters.
    - format_reply: Construct natural language response.
    
    Note: This node may use ToolNode for tool calls; ensure tools are bound to LLM.
    """

    print_function_name()
    try:
        # Extract state variables
        next_action: str = state.get('next_action', '')
        receipt_data: Optional[ReceiptData] = state.get('receipt_data', None)
        pending_question: str = state.get('pending_question', '')
        
        # Convert receipt_data to dict safely if it's a Pydantic model
        receipt_data_dict = None
        if receipt_data is not None:
            if isinstance(receipt_data, ReceiptData):
                receipt_data_dict = {
                    'cost': receipt_data.cost,
                    'date': receipt_data.date,
                    'items': receipt_data.items,
                    'location': receipt_data.location,
                    'category': receipt_data.category
                }
            elif isinstance(receipt_data, dict):
                receipt_data_dict = receipt_data
        
        # Prepare context for the prompt based on next_action
        context_info = ''
        if next_action == 'confirm_receipt' and receipt_data_dict:
            context_info = f"""
Receipt Data Extracted:
- Location: {receipt_data_dict.get('location', 'Unknown')}
- Date: {receipt_data_dict.get('date', 'Unknown')}
- Total Cost: {receipt_data_dict.get('cost', 0)} EUR
- Items: {receipt_data_dict.get('items', 'Unknown')}
- Category: {receipt_data_dict.get('category', 'Unknown')}

The user needs to confirm if this data is correct. Ask for confirmation and let them know the data will be saved to the Excel file.
"""
        elif next_action == 'ocr_failed':
            context_info = """
OCR processing failed. The receipt image could not be processed properly. 
Ask the user to:
1. Send a clearer photo of the receipt
2. Or manually enter the receipt details
"""
        elif next_action == 'query_spending' or pending_question:
            context_info = """
The user is asking about their spending. Use the read_excel tool to retrieve data from the Excel file.
Calculate the answer based on their query (e.g., total spent on groceries, spending in a specific month, etc.).
"""
        else:
            context_info = """
General conversation mode. Respond helpfully to the user's message.
If they ask about spending, use read_excel to get data.
If they need external information, use tavily_search.
"""
        
        # Format the system prompt with context
        prompt = prompts.CHAT_PROMPT.format(
            context=context_info,
            next_action=next_action,
            pending_question=pending_question
        )
        
        # Prepare messages for the LLM - only pass system prompt, not full history
        # The conversation history is already in state['messages'] and will be handled by LangGraph
        messages = [SystemMessage(content=prompt)] + state.get('messages', [])
        
        # Invoke the chat LLM (which has tools bound to it)
        result: AIMessage = safe_invoke(chat_llm, messages=messages)
        
        # Extract the reply text from the result
        reply: str = result.content if result.content else ""
        
        # Clear next_action - set to empty string to stay consistent with str type
        new_next_action: str = ""
        
        # Return updated state with the full AIMessage to preserve tool_calls
        # This allows from_chat_to to properly route to chat_tools_all when tools are called
        return {
            'messages': [result],
            'next_action': new_next_action,
            'latest': reply
        }
        
    except Exception as e:
        print(f'{RED}[NODE] [ERR]{RESET}', e) if DEBUG else None
        traceback.print_exc() if DEBUG else None
        
        # Return error state with helpful message, preserving existing messages
        error_message: str = "I apologize, but I encountered an error processing your request. Please try again or rephrase your question."
        return {
            'messages': state.get('messages', []) + [AIMessage(content=error_message)],
            'next_action': "",
            'latest': error_message
        }



