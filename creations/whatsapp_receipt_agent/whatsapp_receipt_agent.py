def extract_receipt_data(image_input: Union[str, bytes]) -> dict:
    """
    Overview: Preprocesses a receipt image and runs PaddleOCR to extract raw text and potential fields.
    
    Caller Node: ocr_processing
    
    Instructions:
    1. Accept `image_input` which can be either a file path (str) or image data (bytes).
    2. If `image_input` is a path, load the image. If it's bytes, decode it into an image object.
    3. Preprocess the image (e.g., resize, grayscale, denoise) to improve OCR accuracy.
    4. Use PaddleOCR to extract raw text from the preprocessed image.
    5. Attempt to identify and extract key fields like total cost, date, items, location, and category from the raw OCR text.
    6. Return a dictionary containing the extracted fields. If a field cannot be confidently extracted, its value should be None.
    
    Args:
        image_input (Union[str, bytes]): The file path to the receipt image or the image data itself.
    
    Returns:
        dict: A dictionary with keys 'cost', 'date', 'items', 'location', 'category', where values are extracted strings or None if not found.
    """
    try:
        # Load image from path or bytes
        if isinstance(image_input, str):
            image = Image.open(image_input)
        elif isinstance(image_input, bytes):
            image = Image.open(BytesIO(image_input))
        else:
            raise ValueError("image_input must be either a file path (str) or image data (bytes)")
        
        # Preprocess image
        # Convert to grayscale
        image = image.convert('L')
        
        # Resize to max width of 1000px while maintaining aspect ratio
        width, height = image.size
        if width > 1000:
            ratio = 1000 / width
            new_height = int(height * ratio)
            image = image.resize((1000, new_height), Image.Resampling.LANCZOS)
        
        # Apply median filter for noise reduction
        image = image.filter(ImageFilter.MedianFilter(size=3))
        
        # Initialize PaddleOCR
        ocr = PaddleOCR(use_angle_cls=True, lang='en')#, show_log=False)
        
        # Convert PIL image to numpy array for PaddleOCR
        img_array = np.array(image)
        
        # Run OCR
        result = ocr.ocr(img_array)
        
        # Extract text lines from OCR result with robust checking
        lines = []
        if result and len(result) > 0 and result[0] is not None and len(result[0]) > 0:
            for line in result[0]:
                if line and line[1] and line[1][0]:
                    lines.append(line[1][0])
        
        # Initialize result dictionary
        extracted_data = {
            'cost': None,
            'date': None,
            'items': None,
            'location': None,
            'category': None
        }
        
        # If no text extracted, return empty result
        if not lines:
            return extracted_data
        
        # Extract location (first non-empty line)
        for line in lines:
            if line.strip():
                extracted_data['location'] = line.strip()
                break
        
        # Extract total cost - look for "total" in last few lines
        total_keywords = ['total', 'amount', 'sum', 'balance']
        for line in reversed(lines[-5:]):  # Check last 5 lines
            line_lower = line.lower()
            if any(keyword in line_lower for keyword in total_keywords):
                # Extract the last number in the line
                numbers = re.findall(r'\d+\.?\d*', line)
                if numbers:
                    try:
                        extracted_data['cost'] = float(numbers[-1])
                        break
                    except ValueError:
                        continue
        
        # If no total found with keywords, try to find the largest number in last few lines
        if extracted_data['cost'] is None:
            max_cost = 0.0
            for line in reversed(lines[-5:]):
                numbers = re.findall(r'\d+\.?\d*', line)
                for num in numbers:
                    try:
                        num_float = float(num)
                        if num_float > max_cost:
                            max_cost = num_float
                            extracted_data['cost'] = num_float
                    except ValueError:
                        continue
        
        # Extract date - look for common date formats
        date_patterns = [
            r'\d{4}-\d{2}-\d{2}',  # YYYY-MM-DD
            r'\d{2}/\d{2}/\d{4}',  # MM/DD/YYYY or DD/MM/YYYY
            r'\d{2}-\d{2}-\d{4}',  # MM-DD-YYYY or DD-MM-YYYY
            r'\d{2}\.\d{2}\.\d{4}',  # DD.MM.YYYY
        ]
        
        for line in lines:
            for pattern in date_patterns:
                match = re.search(pattern, line)
                if match:
                    extracted_data['date'] = match.group()
                    break
            if extracted_data['date']:
                break
        
        # Extract items - look for lines with quantity and price patterns
        formatted_items = []
        item_patterns = [
            (r'(\d+)\s*[xX]\s*(\d+\.?\d*)', 'qty_x_price'),  # quantity x price
            (r'(\d+)\s*@\s*(\d+\.?\d*)', 'qty_at_price'),  # quantity @ price
            (r'(\w+)\s+(\d+\.?\d*)', 'name_price'),  # item_name price
        ]
        
        for line in lines:
            line_stripped = line.strip()
            # Skip lines that are likely headers, totals, or dates
            if any(keyword in line_stripped.lower() for keyword in ['total', 'date', 'subtotal', 'tax']):
                continue
            if re.match(r'\d{4}-\d{2}-\d{2}', line_stripped):
                continue
            
            # Try to match item patterns and format them
            for pattern, pattern_type in item_patterns:
                match = re.search(pattern, line_stripped)
                if match:
                    if pattern_type == 'qty_x_price':
                        qty = match.group(1)
                        price = match.group(2)
                        # Try to extract item name from the line
                        item_name = re.sub(pattern, '', line_stripped).strip()
                        if not item_name:
                            item_name = 'Item'
                        formatted_items.append(f"{item_name} ({qty} x {price} EUR)")
                    elif pattern_type == 'qty_at_price':
                        qty = match.group(1)
                        price = match.group(2)
                        item_name = re.sub(pattern, '', line_stripped).strip()
                        if not item_name:
                            item_name = 'Item'
                        formatted_items.append(f"{item_name} ({qty} x {price} EUR)")
                    elif pattern_type == 'name_price':
                        item_name = match.group(1)
                        price = match.group(2)
                        formatted_items.append(f"{item_name} (1 x {price} EUR)")
                    break
        
        if formatted_items:
            extracted_data['items'] = ', '.join(formatted_items)
        
        # Infer category based on keywords
        category_keywords = {
            'Coffee': ['coffee', 'cafe', 'starbucks', 'espresso', 'latte', 'cappuccino'],
            'Groceries': ['grocery', 'supermarket', 'walmart', 'target', 'costco', 'food', 'market'],
            'Restaurant': ['restaurant', 'diner', 'bistro', 'grill', 'kitchen'],
            'Gas': ['gas', 'fuel', 'petrol', 'shell', 'bp', 'exxon'],
            'Pharmacy': ['pharmacy', 'drug', 'cvs', 'walgreens', 'prescription'],
            'Electronics': ['electronics', 'best buy', 'apple', 'computer', 'phone'],
            'Clothing': ['clothing', 'fashion', 'apparel', 'h&m', 'zara', 'nike'],
        }
        
        all_text = ' '.join(lines).lower()
        for category, keywords in category_keywords.items():
            if any(keyword in all_text for keyword in keywords):
                extracted_data['category'] = category
                break
        
        return extracted_data
        
    except Exception as e:
        print(f'{RED}[FUNCTION] [ERR]{RESET} Error in extract_receipt_data: {e}')
        traceback.print_exc()
        # Return empty result on error
        return {
            'cost': None,
            'date': None,
            'items': None,
            'location': None,
            'category': None
        }

def validate_ocr_result(ocr_data: dict) -> tuple[bool, str]:
    """
    Overview: Validates the completeness and confidence of the OCR-extracted receipt data.
    
    Caller Node: ocr_processing
    
    Instructions:
    1. Check if essential fields ('cost', 'date', 'items', 'location', 'category') are present and non-empty in the `ocr_data` dictionary.
    2. Validate the format of 'cost' (should be a number) and 'date' (should be YYYY-MM-DD format).
    3. Assess the overall confidence of the extracted data. This could involve checking for common OCR errors or missing critical information.
    4. Return a tuple: (True, 'Valid') if the data is considered valid and complete, otherwise (False, 'Reason for failure').
    
    Args:
        ocr_data (dict): A dictionary containing the raw OCR-extracted fields.
    
    Returns:
        tuple[bool, str]: A boolean indicating validity and a string describing the reason for invalidity if applicable.
    """
    # Define essential fields
    essential_fields = ['cost', 'date', 'items', 'location', 'category']
    
    # Check if ocr_data is None or not a dictionary
    if ocr_data is None or not isinstance(ocr_data, dict):
        return (False, 'OCR data is None or not a dictionary')
    
    # Check if all essential fields are present and non-empty
    for field in essential_fields:
        if field not in ocr_data:
            return (False, f'Missing essential field: {field}')
        
        value = ocr_data[field]
        
        # Check for None values
        if value is None:
            return (False, f'Field {field} is None')
        
        # Check for empty strings (for string fields)
        if isinstance(value, str) and value.strip() == '':
            return (False, f'Field {field} is empty')
    
    # Validate cost is a valid number
    cost = ocr_data.get('cost')
    if cost is not None:
        if not isinstance(cost, (int, float)):
            # Try to convert string to float if possible
            if isinstance(cost, str):
                try:
                    cost = float(cost)
                    # Update the dictionary with converted value
                    ocr_data['cost'] = cost
                except ValueError:
                    return (False, f'Field cost is not a valid number: {cost}')
            else:
                return (False, f'Field cost has invalid type: {type(cost).__name__}')
        
        # Check that cost is non-negative
        if cost < 0:
            return (False, f'Field cost is negative: {cost}')
    
    # Validate date format (YYYY-MM-DD) and that it's a real calendar date
    date = ocr_data.get('date')
    if date is not None and isinstance(date, str):
        date_pattern = r'^\d{4}-\d{2}-\d{2}$'
        if not re.match(date_pattern, date):
            return (False, f'Field date is not in YYYY-MM-DD format: {date}')
        
        # Validate that it's a real calendar date
        try:
            datetime.strptime(date, '%Y-%m-%d')
        except ValueError:
            return (False, f'Field date is not a valid calendar date: {date}')
    
    # All validations passed
    return (True, 'Valid')

def format_items(raw_items: list[dict]) -> str:
    """
    Overview: Converts a list of raw item dictionaries into the required formatted string for storage.
    
    Caller Node: ocr_processing
    
    Instructions:
    1. Iterate through the `raw_items` list, where each item is expected to be a dictionary with keys like 'name', 'quantity', 'price', and optionally 'currency'.
    2. For each item, format it as 'item_name (quantity x price currency)'. If currency is not provided, assume EUR.
    3. Join all formatted item strings with ', ' to create a single single string.
    4. Return the final formatted string.
    
    Args:
        raw_items (list[dict]): A list of dictionaries, each representing an item with its details.
    
    Returns:
        str: A formatted string of items, e.g., 'item1 (quantity x price currency), item2 (quantity x price currency)'.
    """
    # Handle empty list edge case
    if not raw_items:
        return ""
    
    formatted_items = []
    
    for item in raw_items:
        # Validate that item is a dictionary
        if not isinstance(item, dict):
            continue
        
        # Extract values with defaults, handling None values with 'or' operator
        name: str = item.get('name') or 'Item'
        
        # Coerce quantity to int with error handling
        try:
            quantity = int(item.get('quantity', 1))
        except (ValueError, TypeError):
            quantity = 1
        
        # Normalize price to float and format with 2 decimal places
        try:
            price_raw = item.get('price', 0.00)
            price = f"{float(price_raw):.2f}"
        except (ValueError, TypeError):
            price = "0.00"
        
        currency: str = item.get('currency') or 'EUR'
        
        # Format the item string
        formatted_item = f"{name} ({quantity} x {price} {currency})"
        formatted_items.append(formatted_item)
    
    # Join all formatted items with ', '
    return ', '.join(formatted_items)
