""" Schemas """
class ReceiptData(BaseModel):
    """
    Schema for structured receipt data extracted by OCR. Used within the AgentSchema to store parsed receipt information.
    """
    cost: float # Total cost of the receipt.
    date: str # Date of the receipt in YYYY-MM-DD format.
    items: str # Formatted string of items, e.g., 'item1 (quantity x price currency), ...'.
    location: str # Location where the receipt was issued.
    category: str # Automatically determined category of the receipt (e.g., 'Groceries', 'Coffee').

