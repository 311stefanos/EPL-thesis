@tool
def currency_converter(amount: float, from_currency: str, to_currency: str) -> float:
    """
    Overview: Converts an amount from one currency to another using static, predefined exchange rates. This tool is used to normalize currency values, primarily to Euro, for consistent financial reporting. The exchange rates are hardcoded within the tool.
    
    Caller LLM: chat_llm
    
    Outside-the-Tool Work (Tool Handler Function Responsibilities): None. The caller LLM will use the returned float directly in its reasoning or response generation.
    
    Inside-the-Tool Work (Tool Responsibilities): This tool performs the actual currency conversion using a fixed set of exchange rates defined internally.
    
    Instructions:
    1. Accept `amount`, `from_currency`, and `to_currency`.
    2. Access a predefined dictionary of static exchange rates (e.g., {'USD_EUR': 0.85, 'GBP_EUR': 1.15, 'EUR_USD': 1.18, 'EUR_GBP': 0.87}).
    3. Construct a key from `from_currency` and `to_currency` (e.g., 'USD_EUR').
    4. If the direct rate is available, multiply the `amount` by this rate.
    5. If the inverse rate is available (e.g., 'EUR_USD' for 'USD_EUR' conversion), divide the `amount` by this rate.
    6. If neither direct nor inverse rate is found, return the original `amount` and log a warning (or raise an error, depending on desired robustness, but for simplicity, returning original amount is acceptable for now).
    7. Return the converted amount as a float.
    
    State Updates (on the caller function): None. The caller LLM will use the returned float directly.
    
    Args:
        amount (float): The numerical amount to be converted.
        from_currency (str): The currency code of the original amount (e.g., 'USD', 'GBP', 'EUR').
        to_currency (str): The currency code to convert to (e.g., 'EUR').
    
    Returns:
        float: The converted amount.
    """
    # Handle edge case where currencies are the same
    if from_currency == to_currency:
        return amount
    
    # Define static exchange rates
    rates = {
        'USD_EUR': 0.85,
        'GBP_EUR': 1.15,
        'EUR_USD': 1.18,
        'EUR_GBP': 0.87,
        'CHF_EUR': 0.92,
        'EUR_CHF': 1.09
    }
    
    # Construct the direct key
    direct_key = f"{from_currency}_{to_currency}"
    
    # Check if direct rate is available
    if direct_key in rates:
        return amount * rates[direct_key]
    
    # Construct the inverse key
    inverse_key = f"{to_currency}_{from_currency}"
    
    # Check if inverse rate is available
    if inverse_key in rates:
        return amount / rates[inverse_key]
    
    # If neither rate is found, log warning and return original amount
    print(f'{RED}[TOOL] [WARN]{RESET} No exchange rate found for {from_currency} to {to_currency}, returning original amount.')
    return amount

@tool
def tavily_search(query: str) -> dict:
    """
    Overview: Performs a web search using the Tavily API to retrieve external context or information. This tool is used by the `chat` node to clarify ambiguous user queries or to gather additional relevant information for a response.
    
    Caller LLM: chat_llm
    
    Outside-the-Tool Work (Tool Handler Function Responsibilities): None. The caller LLM will use the returned search results directly in its reasoning or response generation.
    
    Inside-the-Tool Work (Tool Responsibilities): This tool sends the `query` to the Tavily search API and returns the search results.
    
    Instructions:
    1. Accept a `query` string.
    2. Use the Tavily API (requires `TAVILY_API_KEY` environment variable) to perform a web search with the given `query`.
    3. Process the raw search results from Tavily.
    4. Extract and format relevant information, such as a direct answer and a list of sources.
    5. Return the processed results in a structured dictionary format (e.g., containing 'answer', 'sources', 'raw_results').
    
    State Updates (on the caller function): None. The caller LLM will use the returned dictionary directly.
    
    Args:
        query (str): The search query string.
    
    Returns:
        dict: A dictionary containing the search results, typically including an 'answer' and 'sources'.
    """
    try:
        # Validate query parameter
        if not query or not query.strip():
            return {
                'answer': 'Error: Search query cannot be empty.',
                'sources': []
            }
        
        # Get the API key from environment variables
        api_key: str = os.getenv('TAVILY_API_KEY')
        
        if not api_key:
            return {
                'answer': 'Error: TAVILY_API_KEY environment variable is not set.',
                'sources': []
            }
        
        # Define the API endpoint
        url: str = 'https://api.tavily.com/search'
        
        # Construct the payload
        payload: dict = {
            'api_key': api_key,
            'query': query.strip(),
            'search_depth': 'basic',
            'include_answer': True,
            'max_results': 5
        }
        
        # Make the POST request
        response = requests.post(url, json=payload, timeout=30)
        
        # Check for HTTP errors
        response.raise_for_status()
        
        # Parse the JSON response
        response_data: dict = response.json()
        
        # Extract the answer
        answer: str = response_data.get('answer', 'No answer found.')
        
        # Extract and format sources as list of dicts for better structure
        sources: list = []
        results: list = response_data.get('results', [])
        
        for result in results:
            title: str = result.get('title', '').strip()
            url: str = result.get('url', '').strip()
            # Only include sources with valid URLs
            if url:
                sources.append({
                    'title': title if title else 'Unknown Title',
                    'url': url
                })
        
        # Return the structured results
        return {
            'answer': answer,
            'sources': sources
        }
        
    except requests.exceptions.RequestException as e:
        print(f'{RED}[TOOL] [ERR]{RESET} Request error in tavily_search: {e}')
        traceback.print_exc()
        return {
            'answer': 'Error: Failed to perform search. Please try again later.',
            'sources': []
        }
    except ValueError as e:
        # Handle JSON decode errors
        print(f'{RED}[TOOL] [ERR]{RESET} JSON parsing error in tavily_search: {e}')
        traceback.print_exc()
        return {
            'answer': 'Error: Failed to parse search response. Please try again later.',
            'sources': []
        }
    except Exception as e:
        print(f'{RED}[TOOL] [ERR]{RESET} Error in tavily_search: {e}')
        traceback.print_exc()
        return {
            'answer': 'Error: An unexpected error occurred. Please try again later.',
            'sources': []
        }

@tool
def write_row_excel(data: ReceiptData) -> dict:
    """
    Overview: Writes a single row of receipt data to the local Excel file (`.xlsx`). This tool handles appending new receipt information to the persistent storage.
    
    Caller LLM: chat_llm (for confirming OCR results), ocr_processing_llm (for saving extracted data)
    
    Outside-the-Tool Work (Tool Handler Function Responsibilities): None. The caller LLM will use the returned status to generate a confirmation or error message for the user.
    
    Inside-the-Tool Work (Tool Responsibilities): This tool manages the file I/O operation to append a new row to the local Excel file.
    
    Instructions:
    1. Receive `data` (a `ReceiptData` object) to be written.
    2. Open the local Excel file (e.g., 'receipts.xlsx' in the agent's directory). If the file does not exist, create it with appropriate headers.
    3. Append the `data` as a new row, mapping `ReceiptData` fields to columns: cost, date, items, location, category.
    4. Save the Excel file.
    5. Return a dictionary indicating success or failure (e.g., `{'status': 'success'}` or `{'status': 'failure', 'reason': 'File not found'}`).
    
    State Updates (on the caller function): None. The caller node will update `state.messages` with confirmation or error messages based on the tool's output.
    
    Args:
        data (ReceiptData): The structured receipt data to write to the Excel file.
    
    Returns:
        dict: A dictionary indicating the status of the operation. For writes: `{'status': 'success'}` or `{'status': 'failure', 'reason': '...'}`.
    """
    try:
        # Validate that data is a ReceiptData instance
        if not isinstance(data, ReceiptData):
            return {'status': 'failure', 'reason': 'Invalid data type: expected ReceiptData instance'}
        
        # Validate required fields are not None or empty
        required_fields = {
            'cost': data.cost,
            'date': data.date,
            'items': data.items,
            'location': data.location,
            'category': data.category
        }
        
        for field_name, field_value in required_fields.items():
            if field_value is None:
                return {'status': 'failure', 'reason': f'Missing required field: {field_name} is None'}
            if isinstance(field_value, str) and field_value.strip() == '':
                return {'status': 'failure', 'reason': f'Missing required field: {field_name} is empty'}
        
        # Validate cost is a valid non-negative number
        if not isinstance(data.cost, (int, float)):
            return {'status': 'failure', 'reason': f'Invalid cost type: expected number, got {type(data.cost).__name__}'}
        if data.cost < 0:
            return {'status': 'failure', 'reason': f'Invalid cost value: {data.cost} is negative'}
        
        # Define the Excel file path in the same directory as the agent
        excel_file: Path = Path(__file__).resolve().parent / 'receipts.xlsx'
        
        # Try to load existing workbook, or create new one if it doesn't exist
        wb = None
        try:
            wb = openpyxl.load_workbook(str(excel_file))
            ws = wb.active
        except FileNotFoundError:
            # File doesn't exist, create new workbook with headers
            wb = openpyxl.Workbook()
            ws = wb.active
            headers: list[str] = ['cost', 'date', 'items', 'location', 'category']
            ws.append(headers)
        
        # Extract data from the ReceiptData object
        row_data: list = [
            data.cost,
            data.date,
            data.items,
            data.location,
            data.category
        ]
        
        # Append the new row
        ws.append(row_data)
        
        # Save the workbook
        wb.save(str(excel_file))
        
        return {'status': 'success'}
        
    except PermissionError as e:
        print(f'{RED}[TOOL] [ERR]{RESET} Permission error writing to Excel file: {e}')
        return {'status': 'failure', 'reason': f'Permission denied: {str(e)}'}
    except OSError as e:
        print(f'{RED}[TOOL] [ERR]{RESET} OS error writing to Excel file: {e}')
        return {'status': 'failure', 'reason': f'File system error: {str(e)}'}
    except Exception as e:
        print(f'{RED}[TOOL] [ERR]{RESET} Error writing to Excel file: {e}')
        traceback.print_exc()
        return {'status': 'failure', 'reason': str(e)}
    finally:
        # Ensure workbook is always closed to prevent file-lock issues
        if wb is not None:
            try:
                wb.close()
            except Exception:
                pass

@tool
def read_excel(query: Optional[dict]) -> dict:
    """
    Overview: Reads receipt data from the local Excel file (`.xlsx`). This tool retrieves stored receipt information, optionally applying filters, for analysis and reporting.
    
    Caller LLM: chat_llm (for answering spending queries)
    
    Outside-the-Tool Work (Tool Handler Function Responsibilities): None. The caller LLM will use the returned data to formulate a user-facing reply.
    
    Inside-the-Tool Work (Tool Responsibilities): This tool manages the file I/O operation to read data from the local Excel file.
    
    Instructions:
    1. Receive an optional `query` dictionary (e.g., `{'category': 'Groceries', 'date': '2023-10'}`).
    2. Open the local Excel file (e.g., 'receipts.xlsx' in the agent's directory).
    3. Read all rows from the Excel file.
    4. If a `query` is provided, filter the rows based on the query parameters (e.g., match 'category' or 'date').
    5. Convert the read (and filtered) data into a list of `ReceiptData` objects or dictionaries.
    6. Return a dictionary containing the retrieved data (e.g., `{'data': List[ReceiptData]}`).
    
    State Updates (on the caller function): None. The caller node will update `state.messages` with the final reply based on the tool's output.
    
    Args:
        query (Optional[dict]): A dictionary of filters to apply when reading data (e.g., `{'category': 'Groceries', 'date': '2023-10'}`). If None, all data is read.
    
    Returns:
        dict: A dictionary containing the retrieved data. For reads: `{'data': List[ReceiptData]}`.
    """
    # Initialize workbook variable to None before try block for proper cleanup
    wb = None
    
    try:
        # Define the Excel file path in the same directory as the agent
        excel_file: Path = Path(__file__).resolve().parent / 'receipts.xlsx'
        
        # Try to load existing workbook
        wb = openpyxl.load_workbook(str(excel_file))
        ws = wb.active
        
        # Read the header row to identify column indices
        headers: list[str] = []
        for cell in ws[1]:
            if cell.value:
                headers.append(str(cell.value).strip().lower())
        
        # Validate that required columns exist
        required_columns = ['cost', 'date', 'items', 'location', 'category']
        column_indices: dict[str, int] = {}
        for col in required_columns:
            if col in headers:
                column_indices[col] = headers.index(col)
            else:
                # Column not found, return empty data
                return {'data': []}
        
        # Read all data rows
        all_data: list[dict] = []
        for row in ws.iter_rows(min_row=2, values_only=True):
            # Skip empty rows
            if not row or all(cell is None for cell in row):
                continue
            
            # Create a dictionary for the row
            row_data: dict = {}
            for col, idx in column_indices.items():
                if idx < len(row):
                    row_data[col] = row[idx]
                else:
                    row_data[col] = None
            
            # Only add rows that have at least some data
            if any(v is not None for v in row_data.values()):
                all_data.append(row_data)
        
        # If no query provided, return all data
        if query is None or not query:
            return {'data': all_data}
        
        # Apply filters based on query parameters
        filtered_data: list[dict] = []
        for row in all_data:
            match = True
            
            # Filter by category (exact match, case-insensitive)
            if 'category' in query and query['category']:
                row_category = str(row.get('category', '')).strip()
                if row_category.lower() != str(query['category']).strip().lower():
                    match = False
            
            # Filter by date (prefix match, e.g., '2023-10' matches '2023-10-15')
            if match and 'date' in query and query['date']:
                row_date = str(row.get('date', '')).strip()
                query_date = str(query['date']).strip()
                if not row_date.startswith(query_date):
                    match = False
            
            # Filter by location (exact match, case-insensitive)
            if match and 'location' in query and query['location']:
                row_location = str(row.get('location', '')).strip()
                if row_location.lower() != str(query['location']).strip().lower():
                    match = False
            
            if match:
                filtered_data.append(row)
        
        return {'data': filtered_data}
        
    except FileNotFoundError:
        # File doesn't exist, return empty data
        print(f'{RED}[TOOL] [WARN]{RESET} Excel file not found, returning empty data.')
        return {'data': []}
        
    except PermissionError as e:
        print(f'{RED}[TOOL] [ERR]{RESET} Permission error reading Excel file: {e}')
        return {'data': []}
        
    except Exception as e:
        print(f'{RED}[TOOL] [ERR]{RESET} Error reading Excel file: {e}')
        traceback.print_exc()
        return {'data': []}
        
    finally:
        # Ensure workbook is always closed to prevent file-lock issues
        if wb is not None:
            try:
                wb.close()
            except Exception:
                pass
